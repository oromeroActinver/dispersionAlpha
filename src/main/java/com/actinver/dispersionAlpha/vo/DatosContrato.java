package com.actinver.dispersionAlpha.vo;

public class DatosContrato {

	private String noContrato;
	private String Periodo;
	private String email;
	private String nameClient;
	private String rfcClient;
	private String nameFile;
	private String dirFile;
	private String password;
	private String fecheGen;

	public String getNoContrato() {
		return noContrato;
	}

	public void setNoContrato(String noContrato) {
		this.noContrato = noContrato;
	}

	public String getPeriodo() {
		return Periodo;
	}

	public void setPeriodo(String periodo) {
		Periodo = periodo;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getNameClient() {
		return nameClient;
	}

	public void setNameClient(String nameClient) {
		this.nameClient = nameClient;
	}

	public String getRfcClient() {
		return rfcClient;
	}

	public void setRfcClient(String rfcClient) {
		this.rfcClient = rfcClient;
	}

	public String getNameFile() {
		return nameFile;
	}

	public void setNameFile(String nameFile) {
		this.nameFile = nameFile;
	}

	public String getDirFile() {
		return dirFile;
	}

	public void setDirFile(String dirFile) {
		this.dirFile = dirFile;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getFecheGen() {
		return fecheGen;
	}

	public void setFecheGen(String fecheGen) {
		this.fecheGen = fecheGen;
	}

}
