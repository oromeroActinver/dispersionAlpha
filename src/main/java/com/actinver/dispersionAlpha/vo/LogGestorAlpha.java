package com.actinver.dispersionAlpha.vo;

public class LogGestorAlpha {

	private String noContrato;
	private String status;
	private String descripcion;
	private String nombreZip;
	private String passwordZip;
	private String correo;

	public String getCorreo() {
		return correo;
	}

	public void setCorreo(String correo) {
		this.correo = correo;
	}

	public String getNoContrato() {
		return noContrato;
	}

	public void setNoContrato(String noContrato) {
		this.noContrato = noContrato;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getDescripcion() {
		return descripcion;
	}

	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}

	public String getNombreZip() {
		return nombreZip;
	}

	public void setNombreZip(String nombreZip) {
		this.nombreZip = nombreZip;
	}

	public String getPasswordZip() {
		return passwordZip;
	}

	public void setPasswordZip(String passwordZip) {
		this.passwordZip = passwordZip;
	}

}
